  Future<void> _initializeServices() async {
    // Request permissions
    bool permissionsGranted = await _bleService.requestPermissions();
    if (!permissionsGranted) {
      _showError("Bluetooth permissions not granted");
      return;
    }

    // Start sensor services
    _sensorService.startSampling();
    _imuFusionService.startAnalysis();

    // Set up sensor data listeners
    _sensorService.accelerometerStream.listen((event) {
      _imuFusionService.addAccelerometerData(event);
    });

    _sensorService.gyroscopeStream.listen((event) {
      _imuFusionService.addGyroscopeData(event);
    });

    _sensorService.magnetometerStream.listen((event) {
      _imuFusionService.addMagnetometerData(event);
    });

    // Listen for fused IMU data
    _imuFusionService.fusedDataStream.listen((data) {
      setState(() {
        _behaviorState = data.behaviorState;
      });
    });

    _bleService.imageStream.listen((data) {
      for (int i = 0; i < data.length; i++) {
        int byte = data[i];

        // Detect Start of Image
        if (!_isReceivingImage &&
            i + 1 < data.length &&
            data[i] == 0xFF &&
            data[i + 1] == 0xD8) {
          _incomingBuffer.clear();
          _isReceivingImage = true;
        }

        if (_isReceivingImage) {
          _incomingBuffer.add(byte);

          // Detect End of Image
          int len = _incomingBuffer.length;
          if (len >= 2 &&
              _incomingBuffer[len - 2] == 0xFF &&
              _incomingBuffer[len - 1] == 0xD9) {
            _handleCompleteImage(Uint8List.fromList(_incomingBuffer));
            _isReceivingImage = false;
            _incomingBuffer.clear();
          }
        }
      }
    });
  }

  Future<void> _handleCompleteImage(Uint8List imageData) async {
    setState(() {
      _currentImageData = imageData;
    });

    try {
      if (Platform.isAndroid) {
        // Request storage permission for Android
        var status = await Permission.storage.request();
        if (!status.isGranted) {
          _showError("Storage permission not granted");
          return;
        }
      } else if (Platform.isIOS) {
        // Request photo library permission for iOS
        var status = await Permission.photos.request();
        if (!status.isGranted) {
          _showError("Photo library permission not granted");
          return;
        }
      }

      Directory? directory;
      if (Platform.isAndroid) {
        // Get the external storage directory for Android
        directory = await getExternalStorageDirectory();

        // Create a Pictures directory if it doesn't exist
        final picturesDir = Directory('${directory!.path}/Pictures');
        if (!await picturesDir.exists()) {
          await picturesDir.create(recursive: true);
        }

        // Create a Necklace directory inside Pictures
        directory = Directory('${picturesDir.path}/Necklace');
        if (!await directory.exists()) {
          await directory.create(recursive: true);
        }
      } else if (Platform.isIOS) {
        // Get the documents directory for iOS
        directory = await getApplicationDocumentsDirectory();

        // Create a Necklace directory
        directory = Directory('${directory.path}/Necklace');
        if (!await directory.exists()) {
          await directory.create(recursive: true);
        }
      } else {
        throw Exception("Unsupported platform");
      }

      // Save the image with timestamp
      final timestamp = DateTime.now().millisecondsSinceEpoch;
      final file = File('${directory.path}/image_$timestamp.jpg');

      // Write the image data to the file
      await file.writeAsBytes(imageData);

      // Show success message
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(
          content: Text('Image saved to: ${file.path}'),
          duration: const Duration(seconds: 3),
        ),
      );
    } catch (e) {
      _showError("Failed to save image: $e");
    }
  }

  void _showError(String message) {
    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(
        content: Text(message),
        backgroundColor: Colors.red,
        duration: const Duration(seconds: 3),
      ),
    );
  }

  Future<void> _startScan() async {
    setState(() {
      _isScanning = true;
      _scanResults = [];
    });

    try {
      await _bleService.startScan();
      // Listen to scan results
      FlutterBluePlus.scanResults.listen((results) {
        setState(() {
          _scanResults = results;
        });
      }, onError: (e) {
        _showError("Error scanning: $e");
      });
    } catch (e) {
      _showError("Error starting scan: $e");
    }

    setState(() {
      _isScanning = false;
    });
  }

  Future<void> _connectToDevice(BluetoothDevice device) async {
    try {
      await _bleService.connect(device);
      setState(() {
        _isConnected = true;
        _connectedDevice = device;
      });
    } catch (e) {
      _showError("Error connecting: $e");
    }
  }

  Future<void> _disconnectDevice() async {
    try {
      await _bleService.disconnect();
      setState(() {
        _isConnected = false;
        _connectedDevice = null;
      });
    } catch (e) {
      _showError("Error disconnecting: $e");
    }
  }

